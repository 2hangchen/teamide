func bindManageMessageApi(appendApi func(apis ...*base.ApiWorker)) {

	appendApi(&base.ApiWorker{Apis: []string{"/manage/message/page"}, Power: base.PowerManageMessagePage, Do: apiManageMessagePage})
	appendApi(&base.ApiWorker{Apis: []string{"/manage/message/list"}, Power: base.PowerManageMessagePage, Do: apiManageMessageList})
	appendApi(&base.ApiWorker{Apis: []string{"/manage/message/one"}, Power: base.PowerManageMessagePage, Do: apiManageMessageOne})
	appendApi(&base.ApiWorker{Apis: []string{"/manage/message/insert"}, Power: base.PowerManageMessageInsert, Do: apiManageMessageInsert})
	appendApi(&base.ApiWorker{Apis: []string{"/manage/message/update"}, Power: base.PowerManageMessageUpdate, Do: apiManageMessageUpdate})
	appendApi(&base.ApiWorker{Apis: []string{"/manage/message/delete"}, Power: base.PowerManageMessageDelete, Do: apiManageMessageDelete})

}

