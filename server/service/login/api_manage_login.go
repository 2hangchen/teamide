func bindManageLoginApi(appendApi func(apis ...*base.ApiWorker)) {

	appendApi(&base.ApiWorker{Apis: []string{"/manage/login/page"}, Power: base.PowerManageLoginPage, Do: apiManageLoginPage})
	appendApi(&base.ApiWorker{Apis: []string{"/manage/login/list"}, Power: base.PowerManageLoginPage, Do: apiManageLoginList})
	appendApi(&base.ApiWorker{Apis: []string{"/manage/login/one"}, Power: base.PowerManageLoginPage, Do: apiManageLoginOne})
	appendApi(&base.ApiWorker{Apis: []string{"/manage/login/insert"}, Power: base.PowerManageLoginInsert, Do: apiManageLoginInsert})
	appendApi(&base.ApiWorker{Apis: []string{"/manage/login/update"}, Power: base.PowerManageLoginUpdate, Do: apiManageLoginUpdate})
	appendApi(&base.ApiWorker{Apis: []string{"/manage/login/delete"}, Power: base.PowerManageLoginDelete, Do: apiManageLoginDelete})

}

